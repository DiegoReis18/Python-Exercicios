#Ler dois valores numéricos, efetuar a adição e mostrar SE o valor for maior que 10

num1= int(input("Digite o primeiro valor: "))
num2= int(input("Digite o segundo valor: "))
soma=num1+num2
if(soma >= 10):
      print("A soma vale: ",soma,", esse valor é maior ou igual a 10")
else:
      print("A soma vale menos de 10")
                
